class Comment < ApplicationRecord
  #===================================================
  # CALLBACKS
  #===================================================

  #===================================================
  # SCOPES
  #===================================================

  #===================================================
  # VALIDATIONS
  #===================================================
  validates :body, presence: true

  #===================================================
  # RELATIONSHIPS
  #===================================================
  belongs_to :post, counter_cache: true, dependent: :destroy
  belongs_to :author, class_name: 'User', foreign_key: :user_id, counter_cache: true, dependent: :destroy

  #===================================================
  # INSTANCE METHODS
  #===================================================
end
